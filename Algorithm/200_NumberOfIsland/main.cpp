#include <bits/stdc++.h>
using namespace std;
class Solution
{
public:
    vector<int> nextX = {1, 0, -1, 0};
    vector<int> nextY = {0, 1, 0, -1};
    queue<pair<int, int>> _queue;
    int sizeX;
    int sizeY;
    void clear(vector<vector<char>> &_grid)
    {
        if (_queue.size() == 0)
        {
            return;
        }
        pair<int, int> p = _queue.front();
        int x = p.first;
        int y = p.second;
        _queue.pop();
        for (int i = 0; i < 4; i++)
        {
            int nextRow = x + nextX[i];
            int nextColumn = y + nextY[i];
            if (nextRow >= 0 && nextRow < sizeX && nextColumn >= 0 && nextColumn < sizeY && _grid[nextRow][nextColumn] == '1')
            {
                _grid[nextRow][nextColumn] = '0';
                _queue.push({nextRow, nextColumn});
            }
        }
        clear(_grid);
    }
    int numIslands(vector<vector<char>> &grid)
    {
        int count = 0;
        sizeX = grid.size();
        sizeY = grid[0].size();
        for (int i = 0; i < sizeX; i++)
        {
            for (int j = 0; j < sizeY; j++)
            {
                cout << i << " , " << j << endl;
                if (grid[i][j] == '1')
                {
                    _queue.push({i, j});
                    clear(grid);
                    count++;
                }
            }
        }
        return count;
    }
};
